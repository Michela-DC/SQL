CREATE TABLE MEAL (
  ID INT AUTO_INCREMENT,
  NAME VARCHAR(255),
  PRICE DOUBLE,
  CALORIES INT,
  PRIMARY KEY (ID)
);

INSERT INTO MEAL(NAME, PRICE, CALORIES)
